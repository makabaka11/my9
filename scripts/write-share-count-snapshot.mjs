import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  parseShareCount,
  resolveShareCountFromD1Local,
  resolveShareCountFromD1Remote,
} from "./share-count-utils.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const snapshotFilePath = path.resolve(__dirname, "../lib/generated/share-count-snapshot.ts");

function loadLocalEnvFiles() {
  for (const file of [".env.local", ".env"]) {
    try {
      process.loadEnvFile(path.resolve(process.cwd(), file));
    } catch {
      // ignore missing env files
    }
  }
}

function readExistingSnapshotCount() {
  if (!existsSync(snapshotFilePath)) return null;
  try {
    const raw = readFileSync(snapshotFilePath, "utf8");
    const match = raw.match(/SHARE_COUNT_SNAPSHOT\s*=\s*(\d+)/);
    if (!match) return null;
    return parseShareCount(match[1]);
  } catch {
    return null;
  }
}

function buildSnapshotContent(count) {
  return `// Auto-generated by scripts/write-share-count-snapshot.mjs.
// This value is snapshotted at build time to avoid runtime requests.

export const SHARE_COUNT_SNAPSHOT = ${count};
`;
}

function hasArg(name) {
  return process.argv.includes(`--${name}`);
}

function resolveTargetEnv() {
  return hasArg("test") ? "test" : "production";
}

async function main() {
  loadLocalEnvFiles();

  const fallbackCount = readExistingSnapshotCount() ?? parseShareCount(process.env.NEXT_PUBLIC_SHARE_COUNT) ?? 0;
  let nextCount = fallbackCount;
  let source = "fallback";
  const targetEnv = resolveTargetEnv();

  if (hasArg("local")) {
    try {
      nextCount = await resolveShareCountFromD1Local(targetEnv);
      source = `d1:${targetEnv}:local`;
    } catch (error) {
      console.warn("[share-count] failed to query local D1, using fallback snapshot.", error);
    }
  } else if (hasArg("remote")) {
    try {
      nextCount = await resolveShareCountFromD1Remote(targetEnv);
      source = `d1:${targetEnv}:remote`;
    } catch (error) {
      console.warn("[share-count] failed to query remote D1, using fallback snapshot.", error);
    }
  } else {
    console.log("[share-count] no query flag provided; keeping existing snapshot/fallback.");
  }

  mkdirSync(path.dirname(snapshotFilePath), { recursive: true });
  const nextContent = buildSnapshotContent(nextCount);
  const currentContent = existsSync(snapshotFilePath) ? readFileSync(snapshotFilePath, "utf8") : "";

  if (currentContent !== nextContent) {
    writeFileSync(snapshotFilePath, nextContent, "utf8");
    console.log(`[share-count] snapshot updated from ${source}: ${nextCount}`);
    return;
  }

  console.log(`[share-count] snapshot unchanged from ${source}: ${nextCount}`);
}

main().catch((error) => {
  console.error("[share-count] unexpected error while writing snapshot:", error);
  process.exitCode = 0;
});
